using Microsoft.VisualStudio.TestTools.UnitTesting;
using SqlBuildManager.Console.Threaded;
using System.IO;

namespace SqlBuildManager.Console.UnitTest
{
    [TestClass]
    public class BuildExecutionContextTest
    {
        [TestMethod]
        public void RunId_AutoGeneratedOnFirstAccess()
        {
            var context = new BuildExecutionContext();
            
            var runId1 = context.RunId;
            var runId2 = context.RunId;
            
            Assert.IsFalse(string.IsNullOrEmpty(runId1));
            Assert.AreEqual(32, runId1.Length); // GUID without dashes
            Assert.AreEqual(runId1, runId2); // Same value on subsequent accesses
        }

        [TestMethod]
        public void RunId_CanBeSetExplicitly()
        {
            var context = new BuildExecutionContext();
            const string customRunId = "MyCustomRunId123";
            
            context.RunId = customRunId;
            
            Assert.AreEqual(customRunId, context.RunId);
        }

        [TestMethod]
        public void DefaultPropertyValues()
        {
            var context = new BuildExecutionContext();
            
            Assert.AreEqual(string.Empty, context.ProjectFileName);
            Assert.AreEqual(string.Empty, context.BuildZipFileName);
            Assert.AreEqual(string.Empty, context.PlatinumDacPacFileName);
            Assert.AreEqual(string.Empty, context.RootLoggingPath);
            Assert.AreEqual(string.Empty, context.WorkingDirectory);
            Assert.IsNull(context.BatchCollection);
            Assert.IsNull(context.BuildDataModel);
        }

        [TestMethod]
        public void Reset_ClearsAllValues()
        {
            var context = new BuildExecutionContext();
            var loggingPath = Path.Combine(Path.GetTempPath(), "logs");
            var workingDir = Path.Combine(Path.GetTempPath(), "work");

            // Set all values
            var originalRunId = context.RunId; // Access to generate
            context.ProjectFileName = "project.xml";
            context.BuildZipFileName = "build.sbm";
            context.PlatinumDacPacFileName = "platinum.dacpac";
            context.RootLoggingPath = loggingPath;
            context.WorkingDirectory = workingDir;
            context.BatchCollection = new SqlSync.SqlBuild.ScriptBatchCollection();
            context.BuildDataModel = SqlSync.SqlBuild.SqlBuildFileHelper.CreateShellSqlSyncBuildDataModel();
            
            // Reset
            context.Reset();
            
            // Verify all cleared (RunId will auto-generate a new one)
            Assert.AreEqual(string.Empty, context.ProjectFileName);
            Assert.AreEqual(string.Empty, context.BuildZipFileName);
            Assert.AreEqual(string.Empty, context.PlatinumDacPacFileName);
            Assert.AreEqual(string.Empty, context.RootLoggingPath);
            Assert.AreEqual(string.Empty, context.WorkingDirectory);
            Assert.IsNull(context.BatchCollection);
            Assert.IsNull(context.BuildDataModel);
            
            // New RunId should be generated after reset
            var newRunId = context.RunId;
            Assert.AreNotEqual(originalRunId, newRunId);
        }

        [TestMethod]
        public void PropertySettersWork()
        {
            var loggingPath = Path.Combine(Path.GetTempPath(), "logs");
            var workingDir = Path.Combine(Path.GetTempPath(), "work");
            var context = new BuildExecutionContext
            {
                ProjectFileName = "test.xml",
                BuildZipFileName = "test.sbm",
                PlatinumDacPacFileName = "test.dacpac",
                RootLoggingPath = loggingPath,
                WorkingDirectory = workingDir
            };
            
            Assert.AreEqual("test.xml", context.ProjectFileName);
            Assert.AreEqual("test.sbm", context.BuildZipFileName);
            Assert.AreEqual("test.dacpac", context.PlatinumDacPacFileName);
            Assert.AreEqual(loggingPath, context.RootLoggingPath);
            Assert.AreEqual(workingDir, context.WorkingDirectory);
        }
    }
}
